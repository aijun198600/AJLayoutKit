import Foundation
import UIKit

public extension UIView {
    
    @discardableResult
    public func width(_ width: AJValue) -> UIView {
        
        return self
    }
    
    @discardableResult
    public func height(_ height: AJValue) -> UIView {
        
        return self
    }
    
    @discardableResult
    public func size(_ value: AJValue) -> UIView {
        
        return self
    }
    
    @discardableResult
    public func size(_ width: AJValue, _ height: AJValue) -> UIView {
        
        return self
    }
}
